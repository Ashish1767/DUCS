.MODEL SMALL
.386
.DATA
M DB "ENTER THE FIRST NUMBER:$"
M1 DB 10,13,"ENTER THE SECOND NUMBER:$"
M2 DB 10,13,"THE SUM IS:$"
A DD ?
B DD ?
C DD ?
COUNT DB 04h 
.CODE
.STARTUP
LEA DX,M 
MOV AH,09H
INT 21H

MOV EBX,0
MOV CX,8

AGAIN:
MOV AH,01 
INT 21H

CMP AL,'A'
JGE L5
SUB AL,30H
JMP L6

L5: SUB AL,37H
L6: SHL EBX,4

ADD BL,AL
LOOP AGAIN
MOV A,EBX 


LEA DX,M1  
MOV AH,09H
INT 21H

MOV EBX,0
MOV CX,8
 
AGAIN1:
MOV AH,01 
INT 21H

CMP AL,'A'
JGE L8
SUB AL,30H
JMP L9
L8: SUB AL,37H
L9: SHL EBX,4
ADD BL,AL
LOOP AGAIN1
MOV B,EBX


MOV AX,WORD PTR A  
MOV BX,WORD PTR B 

ADD AL,BL  
DAA   
MOV BL,AL 

ADC AH,BH 
MOV AL,AH
DAA
MOV BH,AL 

MOV WORD PTR C,BX  


MOV AX,WORD PTR A+2
MOV BX,WORD PTR B+2
ADC AL,BL
DAA
MOV BL,AL
ADC AH,BH
MOV AL,AH
DAA
MOV BH,AL
MOV WORD PTR C+2,BX

LEA DX,M2   
MOV AH,09
INT 21H

MOV BX,WORD PTR C+2


L1: MOV CH,04H
MOV CL,04H
JNC L2
AGAIN3:
MOV DL,1
ADD DL,30H
MOV AH,02
INT 21H

L2: ROL BX,CL
MOV DL,BL
AND DL,0FH
CMP DL,09
JBE L4
ADD DL,37H
L4: ADD DL,30H
MOV AH,02
INT 21H
DEC CH
JNZ L2
MOV BX,WORD PTR C
JNZ L1
MOV BX,WORD PTR C


Q1: MOV CH,04H
MOV CL,04H
Q2: ROL BX,CL
MOV DL,BL
AND DL,0FH
CMP DL,09
JBE Q4
ADD DL,37
Q4: ADD DL,30H
MOV AH,02
INT 21H
DEC CH
JNZ Q2


MOV BX,WORD PTR C
JNZ Q1

MOV AH,4CH
INT 21H
.EXIT
END



