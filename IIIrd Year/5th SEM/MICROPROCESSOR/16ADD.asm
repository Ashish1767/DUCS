.MODEL SMALL
.DATA
M DB"ENTER FIRST NUMBER: $"
N DB 10,13,"ENTER SECOND NUMBER: $"
O DB 10,13,"SUM IS: $"
.CODE
.STARTUP
MOV AX,@DATA
MOV DS,AX

MOV DX,OFFSET M
MOV AH,09H
INT 21H

MOV BX,0
MOV CX,4
AGAIN:
    MOV AH,01H
    INT 21H
    CMP AL,'A'
    JGE EXIT
    SUB AL,30H
    JMP L6
L6:
   SHL BX,4
   ADD BL,AL
   LOOP AGAIN
    

MOV DX,OFFSET N
MOV AH,09H 
INT 21H

MOV DX,0
MOV CX,4
ASH:
    MOV AH,01H
    INT 21H
    CMP AL,'A'
    JGE EXIT
    SUB AL,30H
    JMP L4
L4:
  SHL DX,4
  ADD DL,AL
  LOOP ASH
  
  
  ADD BL,DL
  MOV AL,BL
  DAA
  MOV BL,AL
  
   ADC BH,DH
   MOV AL,BH
   DAA
   MOV BH,AL
   
    


MOV DX,OFFSET O
MOV AH,09H
INT 21H

JNC C
MOV DL,1
ADD DL,30H
MOV AH,02H
INT 21H

C:
 MOV CX,4
 
 
 AS:
 ROL BX,4
 MOV DL,BL
 AND DL,0FH
 ADD DL,30H
 JMP L5
 
L5:
MOV AH,02H
INT 21H
LOOP AS
EXIT:
    MOV AH,4CH
END 
